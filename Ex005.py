n1 = int(input('Digite um valor: '))
n2 = int(input('Digite outro valor: '))
soma = n1 + n2
multiplicacao = n1*n2
divisao = n1/n2
diviInteira = n1//n2
exponente = n1**n2

print('A soma é {}, a multiplicação é {} e a divisão é {}'.format(soma, multiplicacao, divisao))
print('Divisão inteira {} e a potenciação é {}'.format(diviInteira, exponente))